function interleaveQuestions(shortSet, scenarioSet) {
  const examQuestions = [];

  for (let index = 0; index < Math.max(shortSet.length, scenarioSet.length); index += 1) {
    if (shortSet[index]) examQuestions.push(shortSet[index]);
    if (scenarioSet[index]) examQuestions.push(scenarioSet[index]);
  }

  return examQuestions;
}

